const nome = prompt('Digite o seu nome: ');
document.body.innerHTML = `Seu nome é: <strong>${nome}</strong><br / >`;
document.body.innerHTML += `Seu nome tem <strong>${nome.length}</strong> letras<br / >`;
document.body.innerHTML += `A segunda letra do seu nome é <strong>${nome[1]}</strong><br / >`;
document.body.innerHTML += `Qual o primeiro índice da letra o no seu nome? <strong>${nome.indexOf('o')}</strong><br / >`;
document.body.innerHTML += `Qual o ultimo índice da letra o no seu nome? <strong>${nome.lastIndexOf('o')}</strong><br / >`;
document.body.innerHTML += `As ultimas 3 letras do seu nome são: <strong>${nome.slice(-3, nome.length)}</strong><br / >`;
document.body.innerHTML += `As palavras do seu nome são: <strong>${nome.split(' ')}</strong><br / >`;
document.body.innerHTML += `Seu nome com letras maiúsculas <strong>${nome.toUpperCase()}</strong><br / >`;
document.body.innerHTML += `Seu nome com letras minusculas <strong>${nome.toLowerCase()}</strong><br / >`;